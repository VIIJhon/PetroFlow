logger = logging.getLogger(__name__)


class Phase15Status(Enum):
    """Phase 15 system states"""
    INITIALIZING = "initializing"
    HEALTHY = "healthy"
    DEGRADED = "degraded"           # One or more components failed
    CRITICAL = "critical"            # Raft consensus lost
    ATTACK_DETECTED = "attack_detected"  # Intruder detected


@dataclass
class SystemAlarm:
    """Alarm generated by the system"""
    timestamp: float
    sensor_id: str
    alarm_type: str
    severity: str                     # "info", "warning", "critical"
    message: str
    affected_nodes: List[str]
    consensus_reached: bool           # Do 2/3 nodes agree?


class Phase15System:
    """
    Phase 15 Integrated System
    Orchestrates all components to achieve aerospace-grade reliability.
    """
    
    def __init__(
        self,
        node_id: str,
        peer_nodes: List[str],
        security_level: SecurityLevel = SecurityLevel.PRODUCTION
    ):
        """
        Initialize Phase 15 system.
        
        Args:
            node_id: Unique ID of this node (e.g., "petroflow-node1")
            peer_nodes: IDs of peer nodes (e.g., ["petroflow-node2", "petroflow-node3"])
            security_level: Security level (dev/staging/prod/military)
        """
        self.node_id = node_id
        self.peer_nodes = peer_nodes
        self.security_level = security_level
        self.status = Phase15Status.INITIALIZING
        
        # 1. Security Infrastructure (ECDSA)
        logger.info("Initializing PKI (ECDSA P-256)...")
        self.pki_manager = PKIManager(security_level)
        self.ecdsa_engine = ECDSASignatureEngine(self.pki_manager, security_level)
        self.validator = ZeroTrustDataValidator(self.ecdsa_engine)
        logger.info("PKI initialized")
        
        # 2. Distributed Consensus (Raft)
        logger.info("Initializing Raft Cluster (3 nodes)...")
        all_nodes = [node_id] + peer_nodes
        self.raft_cluster = RaftCluster(all_nodes)
        logger.info("Raft initialized")
        
        # 3. Ultra-low latency Shared Memory
        logger.info("Initializing Ring Buffer (50kHz, 10MB)...")
        self.ring_buffer = RingBuffer(f"petroflow_ring_buffer_{node_id}")
        logger.info("Ring Buffer initialized")
        
        # 4. FFT Processing
        logger.info("Initializing Spectral Processor...")
        self.spectral_processor = SpectralProcessor(self.ring_buffer, fft_size=2048)
        logger.info("Spectral Processor initialized")
        
        # 5. PINN with physical constraints
        logger.info("Initializing PINN (Navier-Stokes embedded)...")
        pinn_config = PINNConfig(
            input_size=4,
            output_size=3,
            hidden_sizes=[64, 128, 128, 64],
            learning_rate=1e-3,
            physics_loss_weight=1.0,
            enabled_constraints=[
                PhysicsConstraint.NAVIER_STOKES,
                PhysicsConstraint.CONTINUITY,
                PhysicsConstraint.ENERGY_BALANCE,
            ]
        )
        self.pinn_trainer = PINNTrainer(pinn_config)
        logger.info("PINN initialized")
        
        # 6. RTOS Scheduler
        logger.info("Initializing RTOS Scheduler...")
        self.scheduler = RTOSScheduler()
        self._setup_rtos_tasks()
        logger.info("RTOS Scheduler initialized")
        
        # Metrics
        self.alarms_generated = 0
        self.anomalies_detected = 0
        self.security_events = 0
        self.consensus_failures = 0
        
        logger.info("Phase 15 System fully initialized")
        self.status = Phase15Status.HEALTHY
    
    def _setup_rtos_tasks(self):
        """Configures RTOS tasks with specific priorities"""
        self.scheduler.add_task(
            name="raft_tick",
            priority=RTOSScheduler.Priority.CRITICAL,
            callback=self._raft_tick,
            interval_ms=10  # Every 10ms
        )
        
        self.scheduler.add_task(
            name="fft_processing",
            priority=RTOSScheduler.Priority.HIGH,
            callback=self._process_spectral,
            interval_ms=10  # Every 10ms
        )
        
        self.scheduler.add_task(
            name="pinn_inference",
            priority=RTOSScheduler.Priority.MEDIUM,
            callback=self._run_pinn_inference,
            interval_ms=100  # Every 100ms
        )
        
        self.scheduler.add_task(
            name="metrics_collection",
            priority=RTOSScheduler.Priority.LOW,
            callback=self._collect_metrics,
            interval_ms=1000  # Every 1s
        )
    
    def inject_sensor_reading(
        self,
        sensor_id: str,
        pressure: float,
        velocity: float,
        temperature: float,
        signature_hex: str,
        metadata: Any
    ) -> Tuple[bool, str]:
        """
        Injects sensor reading with Zero-Trust validation.
        
        Pipeline:
        1. Validate ECDSA signature.
        2. Validate physical constraints.
        3. Write to ring buffer.
        4. Return immediately (<1ms target).
        
        Returns:
            Tuple[bool, str]: (accepted, reason)
        """
        # Build reading dictionary matching the exact signed payload structure
        reading = {
            "pressure": pressure,
            "velocity": velocity,
            "temperature": temperature,
        }
        
        # Validate under Zero-Trust
        is_valid, reason = self.validator.validate_sensor_reading(
            reading, sensor_id, signature_hex, metadata
        )
        
        if not is_valid:
            self.security_events += 1
            logger.warning(f"Reading rejected: {reason}")
            return False, reason
        
        # Create sample for ring buffer
        sample = SensorSample(
            timestamp=time.time(),
            sensor_id=sensor_id,
            channel=0,
            value=pressure  # Simplified for demonstration
        )
        
        # Write to shared memory (lock-free, <1us)
        if not self.ring_buffer.write(sample):
            self.anomalies_detected += 1
            logger.warning("Ring buffer overflow")
            return False, "Ring buffer overflow"
        
        return True, "Accepted"
    
    def _raft_tick(self):
        """Executes one Raft tick (every 10ms)"""
        self.raft_cluster.tick_all()
        
        # Verify consensus
        leader = self.raft_cluster._find_leader()
        if leader is None:
            self.consensus_failures += 1
            self.status = Phase15Status.CRITICAL
            logger.error("CONSENSUS LOST - No active LEADER")
    
    def _process_spectral(self):
        """Processes FFT on spectral data (every 10ms)"""
        fft_result = self.spectral_processor.process()
        if fft_result:
            # Silent logging to prevent console pollution
            pass
    
    def _run_pinn_inference(self):
        """Executes PINN inference (every 100ms)"""
        # Read latest samples from buffer
        samples = self.ring_buffer.read_latest()
        if not samples:
            return
        
        # Prepare input for PINN (simplified)
        x = [[sample.sensor_id[0], time.time(), sample.value, 0] for sample in samples[:1]]
        try:
            predictions = self.pinn_trainer.model.predict(x)
            # predictions[0] = [pressure, velocity, temperature]
            # Verify they are physically valid
            logger.debug(f"PINN predictions: P={predictions[0][0]:.2f}, u={predictions[0][1]:.2f}, T={predictions[0][2]:.2f}")
        except Exception as e:
            logger.error(f"Error in PINN inference: {e}")
    
    def _collect_metrics(self):
        """Collects system metrics (every 1s)"""
        leader = self.raft_cluster._find_leader()
        leader_id = leader.node_id if leader else "NONE"
        
        logger.info(
            f"[{self.node_id}] Status={self.status.value} | "
            f"Leader={leader_id} | "
            f"Alarms={self.alarms_generated} | "
            f"Security events={self.security_events} | "
            f"Consensus failures={self.consensus_failures}"
        )
    
    def consensus_vote_on_anomaly(
        self,
        anomaly_type: str,
        severity: str,
        details: Dict[str, Any]
    ) -> Tuple[bool, List[str]]:
        """
        Votes in cluster if there is an anomaly.
        
        Requires 2 out of 3 nodes to agree.
        
        Returns:
            Tuple[bool, List[str]]: (consensus_reached, agreeing_nodes)
        """
        # Simulate voting in Raft
        # In production: replicate command via Raft
        
        leader = self.raft_cluster._find_leader()
        if leader is None:
            return False, []
        
        command = {
            "type": "anomaly_vote",
            "anomaly_type": anomaly_type,
            "severity": severity,
            "details": details,
            "voter": self.node_id,
            "timestamp": time.time(),
        }
        
        success, leader_id = self.raft_cluster.submit_command(command)
        
        if success:
            # Simulate 2/3 quorum
            return True, [leader_id, self.peer_nodes[0]]
        
        return False, []
    
    def raise_alarm(
        self,
        sensor_id: str,
        alarm_type: str,
        severity: str,
        message: str,
        consensus_required: bool = True
    ) -> SystemAlarm:
        """
        Generates alarm in the system.
        
        Args:
            consensus_required: If True, requires voting in Raft
        
        Returns:
            SystemAlarm: Created alarm instance
        """
        alarm = SystemAlarm(
            timestamp=time.time(),
            sensor_id=sensor_id,
            alarm_type=alarm_type,
            severity=severity,
            message=message,
            affected_nodes=[self.node_id],
            consensus_reached=False
        )
        
        if consensus_required:
            consensus, agreeing_nodes = self.consensus_vote_on_anomaly(
                anomaly_type=alarm_type,
                severity=severity,
                details={"sensor": sensor_id, "message": message}
            )
            alarm.consensus_reached = consensus
            alarm.affected_nodes = agreeing_nodes
        
        self.alarms_generated += 1
        
        log_level = logging.WARNING if severity == "warning" else logging.CRITICAL
        logger.log(
            log_level,
            f"ALARM: {alarm_type} | "
            f"Sensor={sensor_id} | "
            f"Consensus={consensus if consensus_required else 'N/A'} | "
            f"Msg={message}"
        )
        
        return alarm
    
    def get_system_metrics(self) -> Dict[str, Any]:
        """Returns complete system metrics"""
        leader = self.raft_cluster._find_leader()
        
        return {
            "node_id": self.node_id,
            "status": self.status.value,
            "timestamp": time.time(),
            "raft": {
                "current_leader": leader.node_id if leader else None,
                "cluster_status": self.raft_cluster.get_cluster_status(),
                "consensus_failures": self.consensus_failures,
            },
            "security": self.ecdsa_engine.get_security_metrics(),
            "validation": self.validator.get_validation_metrics(),
            "ring_buffer": self.ring_buffer.get_status(),
            "system_events": {
                "alarms_generated": self.alarms_generated,
                "anomalies_detected": self.anomalies_detected,
                "security_events": self.security_events,
            }
        }
    
    def run_tick(self):
        """Executes one complete cycle of the RTOS scheduler"""
        self.scheduler.tick()
    
    def shutdown(self):
        """Gracefully shuts down Phase 15 system"""
        logger.info(f"Shutting down Phase 15 on {self.node_id}...")
        self.ring_buffer.close()
        logger.info("Phase 15 shutdown complete")


